package com.example.recar.recar.models;

public class Trabajador {

    private String dni;
    private String nombre;

    public Trabajador() {

    }

    public String getDni() {
        return this.dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getNombre() {
        return this.nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }


}
